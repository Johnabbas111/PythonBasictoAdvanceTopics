#hybrid And hierarchical inheritance in python 


#hybrid inheritance this means more than one form in a program
class BaseClass:
  pass
class Derieve1(BaseClass):
  pass

class Derieve2(BaseClass):
  pass
class Derieve3(Derieve1,Derieve2):
  pass


#hierarchical inheritance
#this is a tree like structure 
class parent:
  pass
class child1(parent):
  pass
class child2(parent):
  pass
  

  

  
  
   
